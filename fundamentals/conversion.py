# Define a list
l = [1, 2, 3, 4, 5]

print(" from  list")
# Convert list to tuple
lt = tuple(l)
print("List to Tuple:", lt)

# Convert list to set
ls = set(l)
print("List to Set:", ls)

print(" from  tuple")

# Define a tuple
t = (6, 7, 8, 9, 10)

# Convert tuple to list
tl = list(t)
print("Tuple to List:", tl)

# Convert tuple to set
ts = set(t)
print("Tuple to Set:", ts)

# Define a set
s = {11, 12, 13, 14, 15}

print(" from  set")
# Convert set to list
sl = list(s)
print("Set to List:", sl)

# Convert set to tuple
st = tuple(s)
print("Set to Tuple:", st)
